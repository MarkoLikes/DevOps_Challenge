    /**
     * M칠todo que se encarga de cargar los datos de la bandeja de entrada
     */
    private void getDatos() {
        lblTotal.setText(String.valueOf(conexion.getTotal()));
        lblDevOps.setText(String.valueOf(conexion.getDevops()));

        for (int i = 0; i < mensajes.size(); i++) {
            this.modelo.addRow(new Object[]{mensajes.get(i).getRemitente(), mensajes.get(i).getAsunto()});
        }
    }

    /**
     * M칠todo que se encarga de cargar los datos del mensaje seleccionado
     */
    private void getMensaje() {
        this.noRegistro = tblMensajes.getSelectedRow();
        lblFecha.setText(mensajes.get(this.noRegistro).getFecha());
        editCuerpo.setText(mensajes.get(this.noRegistro).getCuerpo());
    }

